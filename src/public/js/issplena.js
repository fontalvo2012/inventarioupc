function recorrerIss() {
    var porcentaje =$('#porcentaje').val();
    var entidad = $('#entidad').val();
    const issplena=JSON.parse(plena);
    issplena.forEach(element => {
        if (element.forma=='null') {
            element.entidad = entidad;
            element.valor=parseInt(element.valor)+(parseInt(element.valor)*porcentaje/100);
            console.log(element);
        }
        var item=JSON.stringify(element);

        $.ajax({
            url: '/tarifas',
            type: 'POST',
            datatype: 'json',
            data:{
                item:item
            },
            success: (data) => {
                consultarTarifa();      
            }
          });
    });    
}

function consultarTarifa(){
    var entidad = $('#entidad').val();
    $.ajax({
    url: '/consultartarifas',
    type: 'POST',
    datatype: 'json',
    data:{
        entidad:entidad
    },
    success: (data) => {
        var cadena='';  
        data.forEach(element => {
            cadena+=` 
            <tr>
            <th scope="row">${element.data.cups}</th>
            <td>${element.data.nombre}</td>
            <td>$ ${number_format(element.data.valor, 2)}</td>
            <td><input type='number' id='d${element.data.cd}' class='form-control form-control-sm p-0' placeholder=" $ Dato"></td>
            <td>
            <a href="#" class="btn btn-warning btn-sm" ><i class='fas fa-sync-alt' style='font-size:12px'></i></a>
            <a href="#" class="btn btn-danger btn-sm" onclick="borrarItem(${element.id})" ><i class='far fa-trash-alt' style='font-size:12px'></i></a>
            </td>
        </tr>`;
        });
        $('#vertarifas').html(cadena);      
    }
    });
}

function borrarItem(id) {
        $.ajax({
        url: '/borraritem',
        type: 'POST',
        datatype: 'json',
        data:{
            id:id
        },
        success: (data) => {
            console.log(data);
            consultarTarifa();      
        }
      });
}
function cambiarCantidad(codigo) {
  var cantidad = $('#cantidad' + codigo).val();
  if (cantidad != "") {
    $.ajax({
      url: '/actualizarCantidad',
      type: 'POST',
      datatype: 'json',
      data: {
        codigo,
        cantidad,
        id: $('#id').val()
      },
      success: (data) => {
        if (data == 'si') {
          location.href = "/verPedidoSedes/" + $('#id').val();
        } else {
          $('#cantidad' + codigo).val('');
          alert('Este pedido solo cuenta con ' + data + ' en el Inventario');
        }
      }
    });
  } else {
    alert('No hay valor');
  }

}


